public class Container {
    int n;
    String productName;

    public Container(int n, String productName) {
        this.n = n;
        this.productName = productName;
    }
    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }
}
